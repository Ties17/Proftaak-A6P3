package jsonFiles;

public class Object {
    private double height;
    private int id;
    private String name;
    private int rotation;
    private String type;
    private boolean visible;
    private double width;
    private double x;
    private double y;

    public Object(double height, int id, String name, int rotation, String type, boolean visible, double width, double x, double y) {
        this.height = height;
        this.id = id;
        this.name = name;
        this.rotation = rotation;
        this.type = type;
        this.visible = visible;
        this.width = width;
        this.x = x;
        this.y = y;
    }
}
